const Palette_t *graphics_palettes_find(const char *id)
{
    for (size_t i = 0; _palettes[i].id != NULL; ++i) {
        if (strcasecmp(_palettes[i].id, id) == 0) {
            return &_palettes[i];
        }
    }
    return NULL;
}
